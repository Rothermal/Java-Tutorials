package hello;

public class Dog {
	private int legs;
	private Boolean tail;
	private String hair;

		public Dog(){
	
		}
		public int getlegs(){
			return legs;
		}
		public Boolean getTail(){
			return tail;
		}
		public String getHair(){
			return hair;
		}
		public void setLegs(int legs){
			this.legs = legs;
		}
		public void setTail(boolean tail){
			this.tail = tail;
		}
		public void setHair(String hair){
			this.hair = hair;
		}
}
